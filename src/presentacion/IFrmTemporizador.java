    private void IniciarTemporizador(){
        ActualizaTiempo();
        ActualizaLabel();
        VerificarFin();
    }
    private void ActualizaTiempo(){
        milisegundos--;
        
        if(milisegundos <0){
            milisegundos = 66;
            segundos--;
        }
        
    }
    private void ActualizaLabel(){
        String temporizador = segundos + "s . " + milisegundos + "ms ";
        lblTemp.setText(temporizador);
    }
    private void VerificarFin() {
        if (segundos <= 0 && milisegundos <= 0) {
            mTimer.stop(); 
            JOptionPane.showMessageDialog(this, "Â¡El tiempo se ha agotado!", "Hagan Juego", JOptionPane.INFORMATION_MESSAGE);
            btnIniciar.setEnabled(true); 
        }
        
    }
    
